#include "libft.h"

/*
** Fills the first n chars of s with zeros.
*/

void		ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*p;

	i = 0;
	p = s;
	while (i < n)
	{
		p[i] = '\0';
		i++;
	}
}
